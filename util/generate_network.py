def make_swap_pair(pair):
    parts = pair[1:].split(",")

    a = int(parts[0])
    b = int(parts[1])

    return f"({a}, {b})"


def make_layer(line):
    swap_pairs = ",".join(
        [
            make_swap_pair(pair)
            for pair in line.replace("[", "").replace("]", ",").split("),")
            if len(pair) > 0
        ]
    )

    return f"Layer::new([{swap_pairs}])"


def make_network(vals):
    layers = ",".join([make_layer(line) for line in vals.strip().split("\n")])

    return f"Network::new([{layers}])"


